def show_relationships(table_name: str, account: str, environment: str, fmt: str = 'table') -> str:
    """Show FK relationships for a table (outbound + inbound)."""
    cache_dir = get_cache_dir(account, environment)
    fkeys_data = load_cache(cache_dir, 'fkeys.json')

    if not fkeys_data:
        return (f"No FK cache found for {account}/{environment}.\n"
                f"Run: python3 schema_refresh.py --account {account} --env {environment}")

    canonical = table_name
    # Try to match case from a known table name in fkeys
    all_tables = set()
    for fk in fkeys_data.get('foreign_keys', []):
        all_tables.add(fk.get('fk_table', ''))
        all_tables.add(fk.get('pk_table', ''))
    for pk in fkeys_data.get('primary_keys', []):
        all_tables.add(pk.get('table_name', ''))
    for t in all_tables:
        if t.lower() == table_name.lower():
            canonical = t
            break

    outbound = [fk for fk in fkeys_data.get('foreign_keys', []) if fk.get('fk_table', '').lower() == canonical.lower()]
    inbound = [fk for fk in fkeys_data.get('foreign_keys', []) if fk.get('pk_table', '').lower() == canonical.lower()]
    pk_cols = [pk.get('column_name', '') for pk in fkeys_data.get('primary_keys', []) if pk.get('table_name', '').lower() == canonical.lower()]

    refreshed = fkeys_data.get('_refreshed_at', 'unknown')[:10]
    header = f"[{account}/{environment}] Relationships for: {canonical}\nSource: ODBC cache ({refreshed})\n"

    if fmt == 'json':
        return header + json.dumps({
            'table': canonical,
            'primary_keys': pk_cols,
            'outbound': outbound,
            'inbound': inbound
        }, indent=2)

    lines = [header]
    if pk_cols:
        lines.append(f"Primary Key: {', '.join(pk_cols)}\n")

    if outbound:
        lines.append(f"Outbound FK ({canonical} → other tables) [{len(outbound)}]:")
        rows = [[fk.get('fk_column', ''), fk.get('pk_table', ''), fk.get('pk_column', '')] for fk in sorted(outbound, key=lambda x: x.get('fk_column', ''))]
        lines.append(format_table_rows(['Column', 'References Table', 'References Column'], rows))
    else:
        lines.append("No outbound foreign keys.")

    lines.append('')

    if inbound:
        lines.append(f"Inbound FK (other tables → {canonical}) [{len(inbound)}]:")
        rows = [[fk.get('fk_table', ''), fk.get('fk_column', ''), fk.get('pk_column', '')] for fk in sorted(inbound, key=lambda x: x.get('fk_table', ''))]
        lines.append(format_table_rows(['From Table', 'From Column', 'This Column'], rows))
    else:
        lines.append("No inbound foreign keys.")

    return '\n'.join(lines)


