# Status
# ---------------------------------------------------------------------------

def show_status(account: str, environment: str) -> str:
    """Show cache freshness and record counts for an account/environment."""
    cache_dir = get_cache_dir(account, environment)
    ages = cache_age_days(cache_dir)
    metadata = load_cache(cache_dir, '_metadata.json') or {}

    lines = [f"[{account}/{environment}] Schema Cache Status", f"Cache dir: {cache_dir}\n"]

    for fname, age in ages.items():
        if age is None:
            lines.append(f"  {fname:<30} NOT PRESENT")
        else:
            warn = ' ⚠️  (>90 days)' if age > 90 else ''
            lines.append(f"  {fname:<30} {age} days old{warn}")

    odbc_info = metadata.get('odbc_refresh', {})
    if odbc_info:
        lines.append(f"\nODBC refresh: {odbc_info.get('timestamp', 'unknown')[:10]}")
        lines.append(f"  Tables: {odbc_info.get('tables_count', '?')}, Columns: {odbc_info.get('columns_count', '?')}, FKs: {odbc_info.get('fkeys_count', '?')}")

    custom_info = metadata.get('custom_refresh', {})
    if custom_info:
        lines.append(f"\nCustom refresh: {custom_info.get('timestamp', 'unknown')[:10]}")
        lines.append(f"  Record types: {custom_info.get('custom_record_types_count', '?')}, Fields: {custom_info.get('custom_fields_count', '?')}")

    if not odbc_info and not custom_info:
        lines.append("\nNo cache populated yet.")
        lines.append("Run: python3 schema_refresh.py --account " + account + " --env " + environment)
        lines.append("or:  python3 schema_lookup.py refresh-custom --account " + account + " --env " + environment)

    return '\n'.join(lines)


