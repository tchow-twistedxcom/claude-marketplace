def get_cache_dir(account: str, environment: str) -> str:
    """Get cache directory for a specific account/environment."""
    return os.path.join(CACHE_ROOT, account, environment)


def load_cache(cache_dir: str, filename: str) -> Optional[Dict[str, Any]]:
    """Load a JSON cache file. Returns None if file doesn't exist or is corrupt."""
    path = os.path.join(cache_dir, filename)
    if not os.path.exists(path):
        return None
    try:
        with open(path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return None


def save_cache(cache_dir: str, filename: str, data: Dict[str, Any]) -> None:
    """Save data to a JSON cache file."""
    os.makedirs(cache_dir, exist_ok=True)
    path = os.path.join(cache_dir, filename)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2)


def cache_age_days(cache_dir: str) -> Dict[str, Optional[float]]:
    """Return age in days for each cache file."""
    files = ['tables.json', 'columns.json', 'fkeys.json', 'custom_records.json', 'custom_fields.json']
    ages = {}
    for fname in files:
        path = os.path.join(cache_dir, fname)
        if os.path.exists(path):
            mtime = os.path.getmtime(path)
            age = (datetime.now(timezone.utc).timestamp() - mtime) / 86400
            ages[fname] = round(age, 1)
        else:
            ages[fname] = None
    return ages


def matches_pattern(text: str, pattern: str) -> bool:
    """Match text against a glob pattern or substring (case-insensitive)."""
    if not pattern:
        return True
    t = text.lower()
    p = pattern.lower()
    # Glob pattern
    if '*' in p or '?' in p:
        return fnmatch.fnmatch(t, p)
    # Substring match
    return p in t


def _col_width(rows: List[str], header: str, max_width: int = 50) -> int:
    """Compute column width from data + header."""
    w = len(header)
    for r in rows:
        w = max(w, len(str(r)))
    return min(w, max_width)


def format_table_rows(headers: List[str], rows: List[List[str]], max_col: int = 50) -> str:
    """Format rows as an ASCII table."""
    widths = [_col_width([r[i] for r in rows], h, max_col) for i, h in enumerate(headers)]
    sep = '+' + '+'.join('-' * (w + 2) for w in widths) + '+'
    def fmt_row(cells: List[str]) -> str:
        parts = []
        for i, cell in enumerate(cells):
            cell_str = str(cell or '')
            if len(cell_str) > widths[i]:
                cell_str = cell_str[:widths[i] - 1] + '…'
            parts.append(f' {cell_str:<{widths[i]}} ')
        return '|' + '|'.join(parts) + '|'
    lines = [sep, fmt_row(headers), sep]
    for row in rows:
        lines.append(fmt_row(row))
    lines.append(sep)
    return '\n'.join(lines)


