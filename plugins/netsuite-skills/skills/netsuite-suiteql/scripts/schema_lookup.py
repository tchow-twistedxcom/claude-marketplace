def list_tables(pattern: Optional[str], account: str, environment: str, fmt: str = 'table') -> str:
    """List tables matching a pattern from the ODBC tables cache."""
    cache_dir = get_cache_dir(account, environment)
    tables_data = load_cache(cache_dir, 'tables.json')
    custom_records_data = load_cache(cache_dir, 'custom_records.json')

    results = []

    if tables_data:
        refreshed = tables_data.get('_refreshed_at', 'unknown')[:10]
        for t in tables_data.get('tables', []):
            name = t.get('table_name', '')
            if not pattern or matches_pattern(name, pattern):
                results.append({
                    'table_name': name,
                    'custom': 'Yes' if t.get('is_custom') else 'No',
                    'hidden': 'Yes' if t.get('is_hidden') else 'No',
                    'description': t.get('description', '')
                })
        source = f'ODBC cache ({refreshed})'
    elif custom_records_data:
        refreshed = custom_records_data.get('_refreshed_at', 'unknown')[:10]
        for r in custom_records_data.get('custom_record_types', []):
            name = r.get('scriptid', '')
            if not pattern or matches_pattern(name, pattern):
                results.append({
                    'table_name': name,
                    'custom': 'Yes',
                    'hidden': 'No',
                    'description': r.get('name', '')
                })
        source = f'custom records cache ({refreshed}) — standard tables not available (run schema_refresh.py for full schema)'
    else:
        return (f"No table cache found for {account}/{environment}.\n\n"
                f"To populate:\n"
                f"  Full schema: python3 schema_refresh.py --account {account} --env {environment}\n"
                f"  Custom only: python3 schema_lookup.py refresh-custom --account {account} --env {environment}")

    total = len(results)
    ptn_str = f' matching "{pattern}"' if pattern else ''
    header = f"[{account}/{environment}] Tables{ptn_str} ({total} result{'s' if total != 1 else ''})\nSource: {source}\n"

    if fmt == 'json':
        return header + json.dumps(results, indent=2)

    if fmt == 'csv':
        lines = ['Table Name,Custom,Hidden,Description']
        for r in results:
            lines.append(f"{r['table_name']},{r['custom']},{r['hidden']},{r['description']}")
        return '\n'.join(lines)

    rows = [[r['table_name'], r['custom'], r['hidden'], r['description']] for r in results]
    return header + format_table_rows(['Table Name', 'Custom', 'Hidden', 'Description'], rows)


