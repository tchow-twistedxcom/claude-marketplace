def refresh_custom(account: str, environment: str) -> str:
    """Refresh custom records and custom fields via SuiteQL gateway (no ODBC needed)."""
    # Import execute_query from query_netsuite.py (same directory)
    script_dir = os.path.dirname(os.path.abspath(__file__))
    if script_dir not in sys.path:
        sys.path.insert(0, script_dir)

    try:
        from query_netsuite import execute_query
    except ImportError as e:
        return f"ERROR: Could not import query_netsuite.py: {e}"

    cache_dir = get_cache_dir(account, environment)
    ts = datetime.now(timezone.utc).strftime('%Y-%m-%dT%H:%M:%SZ')

    print(f"Refreshing custom schema for {account}/{environment}...")

    # Query custom record types
    # Note: CustomRecordType uses 'internalid' (not 'id'), 'scriptid', 'name', 'isinactive'
    print("  Querying CustomRecordType...")
    crt_result = execute_query(
        "SELECT internalid, scriptid, name, isinactive FROM CustomRecordType WHERE isinactive = 'F' ORDER BY name",
        account=account,
        environment=environment,
        return_all_rows=True
    )
    if crt_result.get('error'):
        return f"ERROR querying CustomRecordType: {crt_result['error']}"

    custom_records = []
    for r in crt_result.get('records', []):
        custom_records.append({
            'id': r.get('internalid'),
            'scriptid': (r.get('scriptid') or '').lower(),
            'name': r.get('name') or ''
        })

    print(f"  Found {len(custom_records)} custom record types")

    # Query custom record fields (fieldtype = 'RECORD' = body fields on custom record types)
    # Join with CustomRecordType to get the record's scriptid for grouping
    # Note: 'name' = field label, 'fieldvaluetype' = data type, 'recordtype' = parent record internalid
    print("  Querying CustomField (custom record fields)...")
    cf_result = execute_query(
        """SELECT cf.id, cf.scriptid, cf.name AS label, cf.fieldvaluetype, crt.scriptid AS record_scriptid
           FROM CustomField cf
           INNER JOIN CustomRecordType crt ON cf.recordtype = crt.internalid
           WHERE cf.fieldtype = 'RECORD' AND crt.isinactive = 'F'
           ORDER BY crt.scriptid, cf.name""",
        account=account,
        environment=environment,
        return_all_rows=True
    )
    if cf_result.get('error'):
        return f"ERROR querying CustomField: {cf_result['error']}"

    custom_fields_grouped: Dict[str, List[Dict]] = {}
    for r in cf_result.get('records', []):
        applies_to = (r.get('record_scriptid') or '').lower()
        if not applies_to:
            continue
        if applies_to not in custom_fields_grouped:
            custom_fields_grouped[applies_to] = []
        custom_fields_grouped[applies_to].append({
            'id': r.get('id'),
            'scriptid': (r.get('scriptid') or '').lower(),
            'label': r.get('label') or '',
            'field_type': r.get('fieldvaluetype') or '',
            'applies_to': applies_to
        })

    total_fields = sum(len(v) for v in custom_fields_grouped.values())
    print(f"  Found {total_fields} custom fields across {len(custom_fields_grouped)} record types")

    # Save to cache
    save_cache(cache_dir, 'custom_records.json', {
        '_source': 'suiteql',
        '_refreshed_at': ts,
        '_account': account,
        '_environment': environment,
        '_record_count': len(custom_records),
        'custom_record_types': custom_records
    })

    save_cache(cache_dir, 'custom_fields.json', {
        '_source': 'suiteql',
        '_refreshed_at': ts,
        '_account': account,
        '_environment': environment,
        '_record_count': total_fields,
        'custom_fields': custom_fields_grouped
    })

    # Update metadata
    metadata = load_cache(cache_dir, '_metadata.json') or {'account': account, 'environment': environment}
    metadata['custom_refresh'] = {
        'timestamp': ts,
        'custom_record_types_count': len(custom_records),
        'custom_fields_count': total_fields
    }
    save_cache(cache_dir, '_metadata.json', metadata)

    return (f"\nDone. Cached to {cache_dir}/\n"
            f"  custom_records.json: {len(custom_records)} record types\n"
            f"  custom_fields.json: {total_fields} fields across {len(custom_fields_grouped)} record types")


