#!/usr/bin/env python3
"""
NetSuite SuiteQL Schema Lookup

Explore NetSuite table schemas, column definitions, and relationships.
Reads from cached JSON files (~/.cache/netsuite-schema/) generated by:
  - schema_refresh.py (ODBC full schema — all tables/columns/FKs)
  - refresh-custom subcommand (SuiteQL custom records/fields via gateway)

Falls back gracefully when cache is unavailable:
  L1: ODBC cache (tables.json + columns.json + fkeys.json)
  L2: Custom cache (custom_records.json + custom_fields.json)
  L3: Real-time SuiteQL probe (SELECT * WHERE ROWNUM=1 to discover columns)
  L4: Error message with setup instructions

Usage:
  python3 schema_lookup.py describe <TABLE> [--env sb2] [--format table|json|csv]
  python3 schema_lookup.py tables [PATTERN] [--env sb2]
  python3 schema_lookup.py search <PATTERN> [--env sb2]
  python3 schema_lookup.py relationships <TABLE> [--env sb2]
  python3 schema_lookup.py refresh-custom [--env sb2] [--account twx]
  python3 schema_lookup.py status [--env sb2] [--account twx]
"""

