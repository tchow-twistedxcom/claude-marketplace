# ---------------------------------------------------------------------------

def _probe_table(table_name: str, account: str, environment: str) -> Dict[str, Any]:
    """Probe a table's columns via SELECT * WHERE ROWNUM = 1."""
    script_dir = os.path.dirname(os.path.abspath(__file__))
    if script_dir not in sys.path:
        sys.path.insert(0, script_dir)

    try:
        from query_netsuite import execute_query
    except ImportError as e:
        return {'error': f"Cannot import query_netsuite.py: {e}", 'columns': []}

    # Try to get at least one row to discover column names
    result = execute_query(
        f"SELECT * FROM {table_name} WHERE ROWNUM <= 1",
        account=account,
        environment=environment
    )
    if result.get('error'):
        return {'error': result['error'], 'columns': []}

    records = result.get('records', [])
    if records:
        return {'columns': list(records[0].keys()), 'error': None}

    # Table exists but is empty — get columns via a different approach
    # Try forcing a column error to see what's available (NetSuite sometimes reveals column list in errors)
    return {'columns': [], 'error': None, 'note': 'Table exists but is empty (no column metadata available)'}


# ---------------------------------------------------------------------------
# CLI Entry Point
# ---------------------------------------------------------------------------

def parse_args(argv: List[str]) -> Dict[str, Any]:
    """Parse command-line arguments."""
    args = {
        'subcommand': None,
        'target': None,
        'pattern': None,
        'account': DEFAULT_ACCOUNT,
        'environment': DEFAULT_ENVIRONMENT,
        'format': 'table',
        'error': None
    }

    if len(argv) < 2:
        args['error'] = 'missing subcommand'
        return args

    args['subcommand'] = argv[1].lower()

    # Positional argument (table name or pattern)
    i = 2
    if i < len(argv) and not argv[i].startswith('--'):
        args['target'] = argv[i]
        i += 1

    # Named options
    while i < len(argv):
        arg = argv[i]
        if arg in ('--account',) and i + 1 < len(argv):
            args['account'] = argv[i + 1]
            i += 2
        elif arg in ('--env', '--environment') and i + 1 < len(argv):
            args['environment'] = argv[i + 1]
            i += 2
        elif arg in ('--format',) and i + 1 < len(argv):
            args['format'] = argv[i + 1]
            i += 2
        else:
            i += 1

    return args


