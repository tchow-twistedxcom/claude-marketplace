def describe_table(table_name: str, account: str, environment: str, fmt: str = 'table') -> str:
    """
    Describe columns, types, and FK relationships for a table.
    Merges ODBC cache (primary) with custom field labels (enrichment).
    Falls back to custom cache or real-time probe if ODBC cache unavailable.
    """
    cache_dir = get_cache_dir(account, environment)
    cols_data = load_cache(cache_dir, 'columns.json')
    fkeys_data = load_cache(cache_dir, 'fkeys.json')
    custom_fields_data = load_cache(cache_dir, 'custom_fields.json')

    # Find table in ODBC columns cache (case-insensitive)
    odbc_columns = None
    canonical_table = table_name
    if cols_data:
        for key in cols_data.get('columns', {}):
            if key.lower() == table_name.lower():
                canonical_table = key
                odbc_columns = cols_data['columns'][key]
                break

    # Build label map from custom fields (enrichment)
    label_map: Dict[str, str] = {}
    if custom_fields_data:
        for rec_key, fields in custom_fields_data.get('custom_fields', {}).items():
            if rec_key.lower() == table_name.lower():
                for f in fields:
                    sid = f.get('scriptid', '').lower()
                    label = f.get('label', '')
                    if sid and label:
                        label_map[sid] = label

    source_parts = []
    columns_to_show = []  # List of dicts: {name, type, length, description}

    if odbc_columns:
        # Build column list from ODBC cache
        odbc_refreshed = cols_data.get('_refreshed_at', 'unknown')[:10]
        source_parts.append(f'ODBC cache ({odbc_refreshed})')
        for c in odbc_columns:
            name = c.get('column_name', '')
            label = label_map.get(name.lower(), '')
            description = c.get('description') or label or ''
            columns_to_show.append({
                'name': name,
                'type': c.get('data_type', ''),
                'length': str(c.get('length', '') or ''),
                'description': description
            })

    elif table_name.lower().startswith('customrecord_') and custom_fields_data:
        # L2: custom cache for custom record types
        rec_fields = None
        for rec_key, fields in custom_fields_data.get('custom_fields', {}).items():
            if rec_key.lower() == table_name.lower():
                canonical_table = rec_key
                rec_fields = fields
                break
        if rec_fields:
            cf_refreshed = custom_fields_data.get('_refreshed_at', 'unknown')[:10]
            source_parts.append(f'Custom field cache ({cf_refreshed})')
            # Standard custom record columns always present
            for name in ['ID', 'Name', 'Created', 'LastModified', 'Owner', 'ExternalID']:
                columns_to_show.append({'name': name, 'type': '', 'length': '', 'description': ''})
            for f in rec_fields:
                columns_to_show.append({
                    'name': f.get('scriptid', ''),
                    'type': f.get('field_type', ''),
                    'length': '',
                    'description': f.get('label', '')
                })

    else:
        # L3: real-time probe
        probe = _probe_table(table_name, account, environment)
        if probe.get('error'):
            return f"ERROR: Table '{table_name}' not found in cache or via live query.\n{probe['error']}\n\nRun: python3 schema_refresh.py --account {account} --env {environment}\nor: python3 schema_lookup.py refresh-custom --account {account} --env {environment}"
        source_parts.append('real-time probe (column names only — no types)')
        for col_name in probe.get('columns', []):
            columns_to_show.append({'name': col_name, 'type': '', 'length': '', 'description': ''})

    if custom_fields_data and odbc_columns:
        cf_refreshed = custom_fields_data.get('_refreshed_at', 'unknown')[:10]
        source_parts.append(f'custom field labels ({cf_refreshed})')

    source = ' + '.join(source_parts) if source_parts else 'unknown'

    # Build FK info
    fk_outbound: List[Dict] = []
    fk_inbound: List[Dict] = []
    pk_cols: List[str] = []
    if fkeys_data:
        for fk in fkeys_data.get('foreign_keys', []):
            if fk.get('fk_table', '').lower() == canonical_table.lower():
                fk_outbound.append(fk)
            elif fk.get('pk_table', '').lower() == canonical_table.lower():
                fk_inbound.append(fk)
        for pk in fkeys_data.get('primary_keys', []):
            if pk.get('table_name', '').lower() == canonical_table.lower():
                pk_cols.append(pk.get('column_name', ''))

    # Enrich column descriptions with FK info
    fk_out_map: Dict[str, str] = {}
    for fk in fk_outbound:
        fk_out_map[fk.get('fk_column', '').lower()] = f"→ {fk.get('pk_table', '')}.{fk.get('pk_column', '')}"

    col_count = len(columns_to_show)
    header = f"[{account}/{environment}] Table: {canonical_table} ({col_count} column{'s' if col_count != 1 else ''})\nSource: {source}\n"

    if fmt == 'json':
        result = {
            'table': canonical_table,
            'account': account,
            'environment': environment,
            'source': source,
            'columns': columns_to_show,
            'foreign_keys': {'outbound': fk_outbound, 'inbound': fk_inbound},
            'primary_keys': pk_cols
        }
        return header + json.dumps(result, indent=2)

    if fmt == 'csv':
        lines = ['Column Name,Type,Length,Description,FK Reference']
        for c in columns_to_show:
            fk_ref = fk_out_map.get(c['name'].lower(), '')
            lines.append(f"{c['name']},{c['type']},{c['length']},{c['description']},{fk_ref}")
        return '\n'.join(lines)

    # Table format
    rows = []
    for c in columns_to_show:
        fk_ref = fk_out_map.get(c['name'].lower(), '')
        desc = c['description']
        if fk_ref and not desc:
            desc = fk_ref
        elif fk_ref:
            desc = f"{desc}  {fk_ref}"
        rows.append([c['name'], c['type'], c['length'], desc])

    output = header
    output += format_table_rows(['Column Name', 'Type', 'Length', 'Description / FK'], rows)

    if fk_outbound:
        output += '\n\nOutbound FK (this table → other):'
        for fk in sorted(fk_outbound, key=lambda x: x.get('fk_column', '')):
            output += f"\n  {fk.get('fk_column')} → {fk.get('pk_table')}.{fk.get('pk_column')}"

    if fk_inbound:
        output += '\n\nInbound FK (other tables → this):'
        for fk in sorted(fk_inbound, key=lambda x: x.get('fk_table', '')):
            output += f"\n  {fk.get('fk_table')}.{fk.get('fk_column')} → {canonical_table}.{fk.get('pk_column')}"

    if pk_cols:
        output += f'\n\nPrimary Key: {", ".join(pk_cols)}'

    return output


