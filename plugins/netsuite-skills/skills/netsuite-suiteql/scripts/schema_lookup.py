    account = resolve_account(args['account'])
    environment = resolve_environment(args['environment'])
    fmt = args['format']

    if args['error'] or args['subcommand'] in (None, 'help', '--help', '-h'):
        print(USAGE)
        sys.exit(0 if not args['error'] else 1)

    sub = args['subcommand']
    target = args['target']

    if sub == 'describe':
        if not target:
            print("ERROR: describe requires a table name\nUsage: python3 schema_lookup.py describe <TABLE>")
            sys.exit(1)
        print(describe_table(target, account, environment, fmt))

    elif sub == 'tables':
        print(list_tables(target, account, environment, fmt))

    elif sub == 'search':
        if not target:
            print("ERROR: search requires a pattern\nUsage: python3 schema_lookup.py search <PATTERN>")
            sys.exit(1)
        print(search_schema(target, account, environment, fmt))

    elif sub == 'relationships':
        if not target:
            print("ERROR: relationships requires a table name\nUsage: python3 schema_lookup.py relationships <TABLE>")
            sys.exit(1)
        print(show_relationships(target, account, environment, fmt))

    elif sub == 'refresh-custom':
        result = refresh_custom(account, environment)
        print(result)

